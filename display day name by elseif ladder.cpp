#include<iostream>
using namespace std;

int main(){
    int day;
    cout<<"enter the value of day"<<endl;
    cin>>day;
    if(day==1){
        cout<<"sun"<<endl;
    }
    else if(day==2){
        cout<<"mon"<<endl;
    }
     else if(day==3){
        cout<<"tues"<<endl;
    }
     else if(day==4){
        cout<<"wed"<<endl;
    }
     else if(day==5){
        cout<<"thurs"<<endl;
    }
     else if(day==6){
        cout<<"fri"<<endl;
    }
     else if(day==7){
        cout<<"sat"<<endl;
    }
    else{
        cout<<"invalid value"<<endl;
    }
    
    
    
    
    return 0;
}
